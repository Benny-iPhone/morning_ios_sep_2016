import UIKit

class FriendsTableViewController: UITableViewController {

    var tableArray : [Friend] = []
    
    override func viewDidLoad() {
        super.viewDidLoad()
        
        AppManager.manager.getUserFriends { (arr) in
            self.tableArray = arr
            self.tableView.reloadData()
        }
    }


    // MARK: - Table view data source

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return tableArray.count
    }

    
    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let identifier = FriendCell.identifier
        let cell = tableView.dequeueReusableCell(withIdentifier: identifier, for: indexPath) as! FriendCell

        // Configure the cell...
        cell.configure(friend: tableArray[indexPath.row])

        return cell
    }
    

